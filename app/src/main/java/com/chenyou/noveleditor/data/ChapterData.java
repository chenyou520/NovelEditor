package com.chenyou.noveleditor.data;

public class ChapterData {
    private String chapter_name;
    private String chapter_date;
    private String chapter_path;
    private String chapter_words;

    public ChapterData() {
    }

    public String getChapter_name() {
        return chapter_name;
    }

    public void setChapter_name(String chapter_name) {
        this.chapter_name = chapter_name;
    }

    public String getChapter_date() {
        return chapter_date;
    }

    public void setChapter_date(String chapter_date) {
        this.chapter_date = chapter_date;
    }

    public String getChapter_path() {
        return chapter_path;
    }

    public void setChapter_path(String chapter_path) {
        this.chapter_path = chapter_path;
    }

    public String getChapter_words() {
        return chapter_words;
    }

    public void setChapter_words(String chapter_words) {
        this.chapter_words = chapter_words;
    }
}
